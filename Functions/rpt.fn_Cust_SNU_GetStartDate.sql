CREATE FUNCTION [rpt].[fn_Cust_SNU_GetStartDate]() 

RETURNS DATE
AS
BEGIN

DECLARE @SeasonYear INT = CASE WHEN 100*MONTH(GETDATE())+DAY(GETDATE()) > 701 THEN YEAR(GETDATE())
							   ELSE YEAR(GETDATE()) - 1
						  END
DECLARE @StartDate DATE = CAST(CONCAT(@SeasonYear,'0701') AS DATE)

RETURN @StartDate

END





GO
